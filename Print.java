package while_loop;
import java.util.Scanner;
public class Print {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		int n = sc.nextInt();
		int i = 1;
		System.out.println(8);
		while(i <= n) {
			System.out.println(i);
			i=i+1;
			
		}
		
		
	}

}
